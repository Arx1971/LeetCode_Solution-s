	public static boolean search(int nums[], int target) {
		int left = 0;
		int right = nums.length - 1;
		int pivot;
		while (left <= right) {

			pivot = (right + left) / 2;

			if (nums[pivot] == target)
				return true;

			else if (nums[left] < nums[pivot]) {
				if (target >= nums[left] && target < nums[pivot]) {
					right = pivot - 1;
				} else
					left = pivot + 1;
			} else if (nums[left] > nums[pivot]) {
				if (target <= nums[right] && target > nums[pivot])
					left = pivot + 1;
				else
					right = pivot - 1;
			} else {
				while (left < right && nums[left] == nums[pivot]) {
					left++;
				}
				if (right > pivot)
					continue;
				else
					right = pivot - 1;
			}

		}
		return false;
	}
