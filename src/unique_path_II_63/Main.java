	public static int unique_path(int[][] maze) {

		int m = maze.length;
		int n = maze[0].length;

		if (maze[0][0] == 1)
			return 0;

		int[][] dp = new int[m][n];
		dp[0][0] = 1;

		for (int i = 1; i < m; i++) {
			if (maze[i][0] == 1) {
				dp[i][0] = 0;
			} else {
				dp[i][0] = dp[i - 1][0];
			}
		}

		for (int i = 1; i < n; i++) {
			if (maze[0][i] == 1) {
				dp[0][i] = 0;
			} else {
				dp[0][i] = dp[0][i - 1];
			}
		}

		for (int i = 1; i < m; i++) {
			for (int j = 1; j < n; j++) {
				if (maze[i][j] == 1) {
					dp[i][j] = 0;
				} else {
					dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
				}

			}
		}

		return dp[m - 1][n - 1];

	}

